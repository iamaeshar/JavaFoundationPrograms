public class PrimitiveDataTypes {
    public static void main(String[] args) {
        boolean flag = true;

        char ch = 'A';

        byte age = 10;
        short salary = 12500;
        int annualIncome = 3000000;
        long companyBudget = 500000000;

        float dailyExpense = 10.11F;
        double  monthlyExpense = 40.600;

        System.out.println("flag " + flag);
        System.out.println("character " + ch);
        System.out.println("age " + age);
        System.out.println("salary " + salary);
        System.out.println("annual income" + annualIncome);
        System.out.println("company budget" + companyBudget);
        System.out.println("daily expense" + dailyExpense);
        System.out.println("monthly expense" + monthlyExpense);
    }
}
